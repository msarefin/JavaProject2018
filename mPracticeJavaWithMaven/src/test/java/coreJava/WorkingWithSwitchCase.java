package coreJava;

public class WorkingWithSwitchCase {

	public static void main(String[]args) {
		
		int i = 18;
		
		switch(i) {
		
		case(19):System.out.println("The number is greater than 19");
		break;
		
		case(18):System.out.println("the number is 18");
		break;
		
		case(17):System.out.println("The number is not 18");
		break;
		
		default: System.out.println("This is a default statement");
		
		}
		
		
	}
}
